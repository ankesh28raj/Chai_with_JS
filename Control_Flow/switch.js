const month = "Mar"

switch (month) {
    case "Jan":
        console.log("Jan");
        break;
    case "Feb":
        console.log("Feb");
        break;
    case "Mar":
        console.log("March");
        break;
    case "Apr":
        console.log("April");
        break;
    case "May":
        console.log("May");
        break;
    case "June":
        console.log("June");
        break;

    default:
        console.log("Default case matched!");
        break;
}

